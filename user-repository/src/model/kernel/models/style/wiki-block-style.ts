export default class WikiBlockStyle {
    margin:string
    padding:string
    border:string
    background:string
    alignment:string
    
    constructor(margin:string, padding:string, border:string, background:string, alignment:string) {
        this.margin = margin;
        this.padding = padding;
        this.border = border;
        this.background = background;
        this.alignment = alignment;
    }
}